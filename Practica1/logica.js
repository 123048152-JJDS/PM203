// prompt es una función que muestra un cuadro de diálogo para que el usuario
// ingrese un valor y devuelve ese valor como una cadena de texto.
let nombre = prompt("Escribe tu nombre");
console.log("Hola " + nombre + "!!");

// document permite escribir en el documento HTML desde JavaScript,
// manipular el DOM, etc.
document.write("<h1>Hola " + nombre + "!!</h1>");
